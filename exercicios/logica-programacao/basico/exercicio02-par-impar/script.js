const numeroInformado = parseInt(prompt("Digite um número: "));

if (numeroInformado % 2 == 0) {
  alert(`O número ${numeroInformado} é PAR!!`);
} else {
  alert(`O número ${numeroInformado} é ÍMPAR!!`);
}

