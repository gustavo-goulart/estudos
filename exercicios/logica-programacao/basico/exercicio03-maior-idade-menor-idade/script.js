let idade = parseInt(prompt("Digite sua idade: "));

if (idade >= 18) {
  alert(`Você tem ${idade} anos. MAIOR de idade pode entrar!`);
} else {
  alert(`Você tem ${idade} anos. MENOR de idade, NÃO pode entrar!`);
}

